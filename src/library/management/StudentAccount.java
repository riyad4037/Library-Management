    public void Profile(){
        try{
            
            System.out.println(usr+"-account_begun");
            javaconnection con = new javaconnection();
            
            String sql = "select * from account where concat(username, first_name, last_name, id_no,email) like ?";
            PreparedStatement st = con.c.prepareStatement(sql);
            st.setString(1, "%" + jTextField2.getText() + "%");
            ResultSet rs = st.executeQuery();
            
            while (rs.next()) {
                    jTextField6.setText(rs.getString("username"));
                    jTextField5.setText(rs.getString("first_name"));
                    jTextField1.setText(rs.getString("last_name"));
                    jTextField3.setText(rs.getString("department"));
                    jTextField8.setText(rs.getString("id_no"));
                    jTextField7.setText(rs.getString("batch"));
                    jTextField9.setText(rs.getString("gender"));
                    jTextField4.setText(rs.getString("email"));
                    jTextField10.setText(rs.getString("security_question"));
                    jTextField11.setText(rs.getString("security_answer"));
                    jTextField12.setText(rs.getString("password"));
                    byte[] img = rs.getBytes("Image");
                    ImageIcon image = new ImageIcon(img);
                    Image im = image.getImage();
                    Image myImg = im.getScaledInstance(jLabel2.getWidth(), jLabel2.getHeight(),Image.SCALE_SMOOTH);
                    ImageIcon newImage = new ImageIcon(myImg);
                    jLabel2.setIcon(newImage);
                    
            }
            st.close();
            rs.close();
            con.c.close();
        }catch(Exception e){
            
        }
    }
