    public int codechek(String x, String y){
        try{
            
            javaconnection con = new javaconnection();
        String sql = "Select * from admin_code where username = '"+x+"'";
        PreparedStatement st = con.c.prepareStatement(sql);
        ResultSet rs= st.executeQuery();
        while(rs.next()){
            cod= rs.getString("admin_code");
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Admin Code could not found!");
        }
        if(y.equals(cod)){
            return 1;
        }else{
            return 0;
        }
    }

